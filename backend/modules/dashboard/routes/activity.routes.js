const express =
    require("express");

const router =
    express.Router();

/*
|--------------------------------------------------------------------------
| Controllers
|--------------------------------------------------------------------------
*/

const activityController =
    require(
        "../controllers/activity.controller"
    );

/*
|--------------------------------------------------------------------------
| Middleware
|--------------------------------------------------------------------------
*/

const auth =
    require(
        "../../../middleware/auth"
    );

/*
|--------------------------------------------------------------------------
| Activity
|--------------------------------------------------------------------------
*/

router.get(

    "/",
    auth,

    activityController
        .getActivity
);

/*
|--------------------------------------------------------------------------
| Recent Activity
|--------------------------------------------------------------------------
*/

router.get(

    "/recent",
    auth,

    activityController
        .getActivity
);

module.exports =
    router;